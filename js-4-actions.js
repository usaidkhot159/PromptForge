// ── HISTORY ───────────────────────────────────────────
function addToHistory(p) {
  state.history.unshift({ ...p, ts: Date.now() });
  if (state.history.length > 50) state.history = state.history.slice(0, 50);
  localStorage.setItem('pf_history', JSON.stringify(state.history));
}

function renderHistory() {
  const list = document.getElementById('historyList');
  if (!state.history.length) {
    list.innerHTML = `<div class="empty-state">
      <div class="empty-icon">○</div>
      <div class="empty-title">No history yet</div>
      <div class="empty-sub">Generate prompts to see them here</div>
    </div>`;
    return;
  }
  list.innerHTML = state.history.map(p => {
    const cat = CATEGORIES.find(c => c.id === p.catId) || { emoji:'⭐', color:'#2F4156', label:'General' };
    return `<div class="history-item" onclick="openModal('${p.text.replace(/'/g,"\\'")}')">
      <span class="history-cat-badge" style="background:${cat.color}18;color:${cat.color}">${cat.emoji} ${cat.label}</span>
      <span class="history-text">${p.text}</span>
      <span class="history-time">${formatTimeAgo(p.ts)}</span>
    </div>`;
  }).join('');
}

function formatTimeAgo(ts) {
  const s = Math.floor((Date.now() - ts) / 1000);
  if (s < 60)    return 'just now';
  if (s < 3600)  return `${Math.floor(s / 60)}m ago`;
  if (s < 86400) return `${Math.floor(s / 3600)}h ago`;
  return `${Math.floor(s / 86400)}d ago`;
}

function clearHistory() {
  state.history = [];
  localStorage.setItem('pf_history', '[]');
  renderHistory();
  showToast('History cleared');
}

// ── STATS ─────────────────────────────────────────────
function saveStats() {
  localStorage.setItem('pf_stats', JSON.stringify(state.stats));
  updateHeaderCount();
}

function updateHeaderCount() {
  document.getElementById('headerCount').textContent = state.stats.total;
}

function renderStats() {
  const top    = Object.entries(state.stats.byCat).sort((a, b) => b[1] - a[1])[0];
  const topCat = top ? (CATEGORIES.find(c => c.id === top[0])?.label || top[0]) : '—';

  document.getElementById('statsGrid').innerHTML = [
    { v: state.stats.total,        l: 'Total Generated' },
    { v: state.stats.copies,       l: 'Total Copies' },
    { v: state.stats.downloads,    l: 'Downloads' },
    { v: state.favorites.length,   l: 'Favorites Saved' },
    { v: state.history.length,     l: 'History Items' },
    { v: topCat,                   l: 'Top Category' },
  ].map(s => `<div class="stat-card"><div class="stat-value">${s.v}</div><div class="stat-label">${s.l}</div></div>`).join('');

  document.getElementById('catBreakdown').innerHTML = CATEGORIES.map(c => `
    <div class="stat-card">
      <div class="stat-value" style="font-size:24px">${c.emoji}</div>
      <div class="stat-value" style="font-size:24px;color:${c.color}">${state.stats.byCat[c.id] || 0}</div>
      <div class="stat-label">${c.label}</div>
    </div>
  `).join('');
}

// ── RATINGS ───────────────────────────────────────────
function ratePrompt(encodedText, stars) {
  const text = decodeURIComponent(encodedText);
  state.ratings[text] = stars;
  localStorage.setItem('pf_ratings', JSON.stringify(state.ratings));
  document.querySelectorAll('.card-star').forEach(el => {
    const parent = el.closest('.prompt-card');
    if (parent && parent.querySelector('.card-prompt').textContent.includes(text.slice(0, 30))) {
      const idx = [...el.parentNode.children].indexOf(el) + 1;
      el.classList.toggle('lit', idx <= stars);
    }
  });
  showToast(`Rated ${stars} star${stars !== 1 ? 's' : ''}`);
}
