// ── COPY ──────────────────────────────────────────────
async function copyPrompt(btn, text) {
  const decoded = text.replace(/\\'/g, "'").replace(/\\\\/g, '\\');
  try {
    await navigator.clipboard.writeText(decoded);
    btn.classList.add('copied'); btn.textContent = '✓ Copied';
    setTimeout(() => { btn.classList.remove('copied'); btn.textContent = 'Copy'; }, 2000);
    state.stats.copies++; saveStats();
    showToast('Copied to clipboard');
  } catch { showToast('Copy failed — please copy manually'); }
}

async function copyDailyPrompt() {
  try { await navigator.clipboard.writeText(state.dailyPrompt); showToast('Daily prompt copied'); } catch {}
}

async function copyFromModal() {
  try { await navigator.clipboard.writeText(document.getElementById('modalBody').textContent); showToast('Copied'); } catch {}
}

// ── DOWNLOAD ──────────────────────────────────────────
function downloadPrompt(text, type) {
  const decoded = text.replace(/\\'/g, "'").replace(/\\\\/g, '\\');
  if (type === 'txt') {
    const a   = document.createElement('a');
    a.href     = URL.createObjectURL(new Blob([decoded], { type:'text/plain' }));
    a.download = 'prompt.txt';
    a.click();
  } else {
    const w = window.open('', '_blank');
    w.document.write(`<!DOCTYPE html><html><head><style>
      body{font-family:Georgia,serif;padding:48px;line-height:1.8;font-size:15px;color:#1C2B38;max-width:640px;margin:0 auto}
      h1{font-size:20px;margin-bottom:24px;color:#2F4156;border-bottom:1px solid #C8D9E6;padding-bottom:12px}
      p{margin-bottom:24px}
      footer{margin-top:48px;font-size:11px;color:#8FA8B8}
    </style></head><body>
      <h1>AI Prompt — PromptForge</h1>
      <p>${decoded}</p>
      <footer>Generated by PromptForge &bull; ${new Date().toLocaleDateString()}</footer>
    </body></html>`);
    w.document.close();
    setTimeout(() => w.print(), 500);
  }
  state.stats.downloads++; saveStats();
  showToast(`Downloaded .${type}`);
}

// ── SHARE ─────────────────────────────────────────────
function sharePrompt(text) {
  const decoded = text.replace(/\\'/g, "'").replace(/\\\\/g, '\\');
  if (navigator.share) {
    navigator.share({ title:'PromptForge', text: decoded }).catch(() => {});
  } else {
    navigator.clipboard.writeText(decoded).then(() => showToast('Copied for sharing'));
  }
}

// ── FAVORITES ─────────────────────────────────────────
function toggleFavorite(btn, text, tagsEncoded, diff, catId) {
  const decoded = text.replace(/\\'/g, "'").replace(/\\\\/g, '\\');
  const tags    = JSON.parse(decodeURIComponent(tagsEncoded));
  const idx     = state.favorites.findIndex(f => f.text === decoded);
  if (idx >= 0) {
    state.favorites.splice(idx, 1);
    btn.classList.remove('active');
    showToast('Removed from favorites');
  } else {
    state.favorites.push({ text: decoded, tags, diff, catId });
    btn.classList.add('active');
    showToast('Saved to favorites');
  }
  localStorage.setItem('pf_favorites', JSON.stringify(state.favorites));
}

